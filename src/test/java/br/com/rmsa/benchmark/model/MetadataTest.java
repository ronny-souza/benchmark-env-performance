package br.com.rmsa.benchmark.model;

import br.com.rmsa.benchmark.model.transport.MetadataDTO;
import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.junit.jupiter.MockitoExtension;

@ExtendWith(MockitoExtension.class)
class MetadataTest {

    @Test
    @DisplayName("Checking if id is not generated by new instance of class")
    void checkingIfIdIsNotGeneratedByNewInstanceOfClass() {
        Metadata metadata = new Metadata();
        Assertions.assertNull(metadata.getId());
    }

    @Test
    @DisplayName("Checking whether the fields were filled out correctly")
    void checkingWhetherTheFieldsWereFilledOutCorrectly() {
        MetadataDTO metadataDTO = getMetadataDTO();
        Metadata metadata = new Metadata(metadataDTO);

        Assertions.assertNull(metadata.getId());
        Assertions.assertNotNull(metadata.getFirstname());
        Assertions.assertNotNull(metadata.getLastname());
        Assertions.assertNotNull(metadata.getEmail());
        Assertions.assertNotNull(metadata.getAge());
        Assertions.assertNotNull(metadata.getGender());
        Assertions.assertNotNull(metadata.getPhone());
        Assertions.assertNotNull(metadata.getAddress());
        Assertions.assertNotNull(metadata.getCity());
        Assertions.assertNotNull(metadata.getState());
        Assertions.assertNotNull(metadata.getZipCode());
        Assertions.assertNotNull(metadata.getCountry());
        Assertions.assertNotNull(metadata.getJob());
    }

    private static MetadataDTO getMetadataDTO() {
        MetadataDTO metadataDTO = new MetadataDTO();
        metadataDTO.setFirstname("Firstname");
        metadataDTO.setLastname("Lastname");
        metadataDTO.setEmail("email@example.com");
        metadataDTO.setAge(23);
        metadataDTO.setGender("Male");
        metadataDTO.setPhone("(99) 99999-9999");
        metadataDTO.setAddress("Address");
        metadataDTO.setCity("City");
        metadataDTO.setState("State");
        metadataDTO.setZipCode("ZipCode");
        metadataDTO.setCountry("Country");
        metadataDTO.setJob("Job");
        return metadataDTO;
    }
}